// Helper function to create an icon element
function createIcon(iconName, size = 20, color = 'currentColor') {
  const iconSvg = ICONS[iconName];
  if (!iconSvg) {
    console.warn(`Icon "${iconName}" not found`);
    return '';
  }

  // Replace size and color in the SVG string
  return iconSvg
    .replace(/width="\d+"/, `width="${size}"`)
    .replace(/height="\d+"/, `height="${size}"`)
    .replace(/fill="currentColor"/, `fill="${color}"`);
}

// Helper function to get inline icon HTML for use in innerHTML
function getIconHTML(iconName, className = '', style = '') {
  const iconSvg = ICONS[iconName];
  if (!iconSvg) return '';

  const classAttr = className ? ` class="${className}"` : '';
  const styleAttr = style ? ` style="${style}"` : '';

  return iconSvg.replace('<svg', `<svg${classAttr}${styleAttr}`);
}

